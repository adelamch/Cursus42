#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign_minus;
	int	num;

	i = 0;
	sign_minus = 1;
	num = 0;
	while ((str[i]) && (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r'))
		i++;
	if (str[i] == '-')
		sign_minus = -sign_minus;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	return (num * sign_minus);
}
